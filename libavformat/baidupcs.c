static char *pcs_decrypt(const char *data, int len)
{
    char *result = av_malloc(len + 1);
    if (!result)
        return NULL;
    struct AVRC4 *rc4 = av_rc4_alloc();
    if (!rc4) {
        av_free(result);
        return NULL;
    }
    extern const char MTSP_RC4_KEY[16];
    av_rc4_init(rc4, MTSP_RC4_KEY, 128, 1);
    av_rc4_crypt(rc4, result, data, len, NULL, 1);
    result[len] = '\0';
    av_free(rc4);
    return result;
}

char *pcs_get_user_agent(enum PCSUserAgentType type)
{
    if (type == PCS_ANDROID_UA)
        return pcs_decrypt(PCS_ANDROID_USER_AGENT, sizeof(PCS_ANDROID_USER_AGENT));
    if (type == PCS_WIN_UA)
        return pcs_decrypt(PCS_WIN_USER_AGENT, sizeof(PCS_WIN_USER_AGENT));
    return NULL;
}

char *pcs_get_referer(void)
{
    return pcs_decrypt(PCS_REFERER, sizeof(PCS_REFERER));
}

char *pcs_get_ssl_public_key(void)
{
    return pcs_decrypt(PCS_SSL_PUBLIC_KEY, sizeof(PCS_SSL_PUBLIC_KEY));
}
